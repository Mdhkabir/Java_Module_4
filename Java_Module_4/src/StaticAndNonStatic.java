
public class StaticAndNonStatic {
	
	public static int p = 200; // This is Global static variable
	
	public int q = 100; // This is Global non static variable


	//static method
	public static void first_function(){
		int abc = 20; // This abc is a local variable
	}
	// non static method
	public void second_function(){
		int xyz = 100; //This abc is a local variable
	}
	
	
	
	
	
	
	
}
